// Configura√ß√µes (ajuste conforme necess√°rio)
const SUPABASE_URL = 'https://ldlxebhnkayiwksipvyc.supabase.co'
const SUPABASE_SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY || 'sua_service_role_key_aqui'

// Dados de teste √∫nicos
const TEST_EMAIL = `teste-simple-${Date.now()}@exemplo.com`
const TEST_COMPANY = {
  name: 'Empresa Teste Simples',
  contact_name: 'Jo√£o Teste Simples',
  contact_email: TEST_EMAIL,
  contact_phone: '(11) 77777-7777',
  cnpj: '11.222.333/0001-44',
  address_street: 'Rua Simples, 123',
  address_city: 'S√£o Paulo',
  address_state: 'SP',
  address_zip_code: '01234-567',
  stripe_customer_id: `cus_simple_${Date.now()}`,
  stripe_subscription_id: `sub_simple_${Date.now()}`,
  subscription_status: 'active',
  max_collaborators: 3,
  current_students: 0,
  is_active: true
}

class SimpleTest {
  constructor() {
    this.testResults = []
    this.company = null
    this.authUser = null
    this.profile = null
    this.companyUser = null
  }

  log(message, type = 'info') {
    const timestamp = new Date().toISOString()
    const prefix = type === 'error' ? '‚ùå' : type === 'success' ? '‚úÖ' : '‚ÑπÔ∏è'
    console.log(`${prefix} [${timestamp}] ${message}`)
    this.testResults.push({ timestamp, type, message })
  }

  async runTest() {
    console.log('üß™ TESTE SIMPLES DO FLUXO')
    console.log('=========================')
    console.log(`üìß Email de teste: ${TEST_EMAIL}`)
    console.log('')

    this.log('üöÄ Iniciando teste simples...')
    
    try {
      // Verificar se temos a service role key
      if (!SUPABASE_SERVICE_KEY || SUPABASE_SERVICE_KEY === 'sua_service_role_key_aqui') {
        throw new Error('SUPABASE_SERVICE_ROLE_KEY n√£o configurada. Configure no arquivo .env')
      }

      // 1. Testar conex√£o com Supabase
      await this.testConnection()
      
      // 2. Testar cria√ß√£o de empresa
      await this.testCompanyCreation()
      
      // 3. Testar cria√ß√£o de usu√°rio
      await this.testUserCreation()
      
      // 4. Testar vincula√ß√£o
      await this.testLinking()
      
      // 5. Testar cria√ß√£o de perfil
      await this.testProfileCreation()
      
      // 6. Testar cria√ß√£o de company_user
      await this.testCompanyUserCreation()
      
      // 7. Testar acesso aos dados
      await this.testDataAccess()
      
      // 8. Limpeza
      await this.cleanup()
      
      this.log('üéâ Teste simples finalizado com sucesso!', 'success')
      this.printSummary()
      
    } catch (error) {
      this.log(`‚ùå Erro no teste: ${error.message}`, 'error')
      console.error(error)
      await this.cleanup()
      process.exit(1)
    }
  }

  async testConnection() {
    this.log('üîå Testando conex√£o com Supabase...')
    
    try {
      // Teste simples de conex√£o
      const response = await fetch(`${SUPABASE_URL}/rest/v1/companies?select=count`, {
        headers: {
          'apikey': SUPABASE_SERVICE_KEY,
          'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`
        }
      })

      if (!response.ok) {
        throw new Error(`Erro de conex√£o: ${response.status} ${response.statusText}`)
      }

      this.log('‚úÖ Conex√£o com Supabase estabelecida', 'success')
      
    } catch (error) {
      this.log(`‚ùå Erro de conex√£o: ${error.message}`, 'error')
      throw error
    }
  }

  async testCompanyCreation() {
    this.log('üè¢ Testando cria√ß√£o de empresa...')
    
    try {
      const response = await fetch(`${SUPABASE_URL}/rest/v1/companies`, {
        method: 'POST',
        headers: {
          'apikey': SUPABASE_SERVICE_KEY,
          'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`,
          'Content-Type': 'application/json',
          'Prefer': 'return=representation'
        },
        body: JSON.stringify(TEST_COMPANY)
      })

      if (!response.ok) {
        const error = await response.text()
        throw new Error(`Erro ao criar empresa: ${error}`)
      }

      const company = await response.json()
      this.company = company[0]
      this.log(`‚úÖ Empresa criada: ${company[0].name}`, 'success')
      this.log(`   ID: ${company[0].id}`, 'info')
      
    } catch (error) {
      this.log(`‚ùå Erro ao criar empresa: ${error.message}`, 'error')
      throw error
    }
  }

  async testUserCreation() {
    this.log('üë§ Testando cria√ß√£o de usu√°rio...')
    
    try {
      const response = await fetch(`${SUPABASE_URL}/auth/v1/admin/users`, {
        method: 'POST',
        headers: {
          'apikey': SUPABASE_SERVICE_KEY,
          'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`,
          'Content-Type': 'application/json'
        },
        body: JSON.stringify({
          email: TEST_EMAIL,
          password: 'Teste123!',
          email_confirm: true,
          user_metadata: {
            role: 'company',
            company_id: this.company.id,
            company_name: this.company.name
          }
        })
      })

      if (!response.ok) {
        const error = await response.text()
        throw new Error(`Erro ao criar usu√°rio: ${error}`)
      }

      const user = await response.json()
      this.authUser = user
      this.log(`‚úÖ Usu√°rio criado: ${user.email}`, 'success')
      this.log(`   ID: ${user.id}`, 'info')
      
    } catch (error) {
      this.log(`‚ùå Erro ao criar usu√°rio: ${error.message}`, 'error')
      throw error
    }
  }

  async testLinking() {
    this.log('üîó Testando vincula√ß√£o empresa-usu√°rio...')
    
    try {
      const response = await fetch(`${SUPABASE_URL}/rest/v1/companies?id=eq.${this.company.id}`, {
        method: 'PATCH',
        headers: {
          'apikey': SUPABASE_SERVICE_KEY,
          'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`,
          'Content-Type': 'application/json',
          'Prefer': 'return=representation'
        },
        body: JSON.stringify({ auth_user_id: this.authUser.id })
      })

      if (!response.ok) {
        const error = await response.text()
        throw new Error(`Erro ao vincular empresa: ${error}`)
      }

      this.log('‚úÖ Empresa vinculada ao usu√°rio', 'success')
      
    } catch (error) {
      this.log(`‚ùå Erro ao vincular empresa: ${error.message}`, 'error')
      throw error
    }
  }

  async testProfileCreation() {
    this.log('üìã Testando cria√ß√£o de perfil...')
    
    try {
      const response = await fetch(`${SUPABASE_URL}/rest/v1/profiles`, {
        method: 'POST',
        headers: {
          'apikey': SUPABASE_SERVICE_KEY,
          'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`,
          'Content-Type': 'application/json',
          'Prefer': 'return=representation'
        },
        body: JSON.stringify({
          id: this.authUser.id,
          role: 'company',
          email: TEST_EMAIL,
          name: TEST_COMPANY.contact_name,
          created_at: new Date().toISOString(),
          updated_at: new Date().toISOString()
        })
      })

      if (!response.ok) {
        const error = await response.text()
        throw new Error(`Erro ao criar perfil: ${error}`)
      }

      const profile = await response.json()
      this.profile = profile[0]
      this.log(`‚úÖ Perfil criado com role: ${profile[0].role}`, 'success')
      
    } catch (error) {
      this.log(`‚ùå Erro ao criar perfil: ${error.message}`, 'error')
      throw error
    }
  }

  async testCompanyUserCreation() {
    this.log('üë• Testando cria√ß√£o de company_user...')
    
    try {
      const response = await fetch(`${SUPABASE_URL}/rest/v1/company_users`, {
        method: 'POST',
        headers: {
          'apikey': SUPABASE_SERVICE_KEY,
          'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`,
          'Content-Type': 'application/json',
          'Prefer': 'return=representation'
        },
        body: JSON.stringify({
          email: TEST_EMAIL,
          company_id: this.company.id,
          auth_user_id: this.authUser.id,
          name: TEST_COMPANY.contact_name,
          is_active: true,
          created_at: new Date().toISOString(),
          updated_at: new Date().toISOString()
        })
      })

      if (!response.ok) {
        const error = await response.text()
        throw new Error(`Erro ao criar company user: ${error}`)
      }

      const companyUser = await response.json()
      this.companyUser = companyUser[0]
      this.log(`‚úÖ Company user criado: ${companyUser[0].name}`, 'success')
      
    } catch (error) {
      this.log(`‚ùå Erro ao criar company user: ${error.message}`, 'error')
      throw error
    }
  }

  async testDataAccess() {
    this.log('üìä Testando acesso aos dados...')
    
    try {
      // Testar busca de empresa por auth_user_id
      const companyResponse = await fetch(`${SUPABASE_URL}/rest/v1/companies?auth_user_id=eq.${this.authUser.id}`, {
        headers: {
          'apikey': SUPABASE_SERVICE_KEY,
          'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`
        }
      })

      if (!companyResponse.ok) {
        throw new Error('Erro ao buscar empresa')
      }

      const companyData = await companyResponse.json()
      
      if (companyData.length === 0 || companyData[0].id !== this.company.id) {
        throw new Error('Dados da empresa n√£o correspondem')
      }

      this.log('‚úÖ Acesso aos dados da empresa confirmado', 'success')

      // Testar busca de company_user
      const companyUserResponse = await fetch(`${SUPABASE_URL}/rest/v1/company_users?auth_user_id=eq.${this.authUser.id}`, {
        headers: {
          'apikey': SUPABASE_SERVICE_KEY,
          'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`
        }
      })

      if (!companyUserResponse.ok) {
        throw new Error('Erro ao buscar company user')
      }

      const companyUserData = await companyUserResponse.json()
      
      if (companyUserData.length === 0) {
        throw new Error('Company user n√£o encontrado')
      }

      this.log('‚úÖ Acesso aos dados de company_user confirmado', 'success')
      
    } catch (error) {
      this.log(`‚ùå Erro ao testar acesso: ${error.message}`, 'error')
      throw error
    }
  }

  async cleanup() {
    this.log('üßπ Fazendo limpeza...')
    
    try {
      // Deletar company_user
      if (this.companyUser) {
        await fetch(`${SUPABASE_URL}/rest/v1/company_users?id=eq.${this.companyUser.id}`, {
          method: 'DELETE',
          headers: {
            'apikey': SUPABASE_SERVICE_KEY,
            'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`
          }
        })
      }

      // Deletar perfil
      if (this.profile) {
        await fetch(`${SUPABASE_URL}/rest/v1/profiles?id=eq.${this.profile.id}`, {
          method: 'DELETE',
          headers: {
            'apikey': SUPABASE_SERVICE_KEY,
            'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`
          }
        })
      }

      // Deletar empresa
      if (this.company) {
        await fetch(`${SUPABASE_URL}/rest/v1/companies?id=eq.${this.company.id}`, {
          method: 'DELETE',
          headers: {
            'apikey': SUPABASE_SERVICE_KEY,
            'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`
          }
        })
      }

      // Deletar usu√°rio de autentica√ß√£o
      if (this.authUser) {
        await fetch(`${SUPABASE_URL}/auth/v1/admin/users/${this.authUser.id}`, {
          method: 'DELETE',
          headers: {
            'apikey': SUPABASE_SERVICE_KEY,
            'Authorization': `Bearer ${SUPABASE_SERVICE_KEY}`
          }
        })
      }

      this.log('‚úÖ Limpeza conclu√≠da', 'success')
      
    } catch (error) {
      this.log(`‚ö†Ô∏è Erro na limpeza: ${error.message}`, 'error')
    }
  }

  printSummary() {
    console.log('\nüìã RESUMO DO TESTE SIMPLES')
    console.log('==========================')
    
    const successCount = this.testResults.filter(r => r.type === 'success').length
    const errorCount = this.testResults.filter(r => r.type === 'error').length
    const infoCount = this.testResults.filter(r => r.type === 'info').length
    
    console.log(`‚úÖ Sucessos: ${successCount}`)
    console.log(`‚ùå Erros: ${errorCount}`)
    console.log(`‚ÑπÔ∏è Informa√ß√µes: ${infoCount}`)
    
    console.log('\nüìä Dados Testados:')
    console.log(`üè¢ Empresa: ${this.company?.name || 'N/A'}`)
    console.log(`üë§ Usu√°rio: ${this.authUser?.email || 'N/A'}`)
    console.log(`üìã Perfil: ${this.profile?.role || 'N/A'}`)
    console.log(`üë• Company User: ${this.companyUser?.name || 'N/A'}`)
    
    if (errorCount === 0) {
      console.log('\nüéâ TODOS OS TESTES PASSARAM!')
      console.log('‚úÖ Conex√£o com Supabase funcionando')
      console.log('‚úÖ Cria√ß√£o de empresa funcionando')
      console.log('‚úÖ Cria√ß√£o de usu√°rio funcionando')
      console.log('‚úÖ Vincula√ß√£o empresa-usu√°rio funcionando')
      console.log('‚úÖ Cria√ß√£o de perfil funcionando')
      console.log('‚úÖ Cria√ß√£o de company_user funcionando')
      console.log('‚úÖ Acesso aos dados funcionando')
      console.log('\nüöÄ Sistema pronto para produ√ß√£o!')
    } else {
      console.log('\n‚ö†Ô∏è ALGUNS TESTES FALHARAM!')
      console.log('Verifique os logs acima para detalhes.')
    }
  }
}

